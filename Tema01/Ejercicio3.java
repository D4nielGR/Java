/* Ejercicio 3
    Escribe un programa que muestre por pantalla 10 palabras en inglés junto a su correspondiente traducción al castellano. 
    Las palabras deben estar distribuidas en dos columnas y alineadas a la izquierda. 
    Pista: Se puede insertar un tabulador mediante \t.
*/
public class Ejercicio3 {
  public static void main (String[] args) {
    System.out.println("INGLÉS \t ESPAÑOL");
    System.out.println("water  \t agua");
    System.out.println("blue   \t azul");
    System.out.println("green  \t verde");
    System.out.println("brown  \t marrón");
    System.out.println("yellow \t amarillo");
    System.out.println("red    \t rojo");
    System.out.println("purple \t violeta");
    System.out.println("pink   \t rosa");
    System.out.println("black  \t negro");
    System.out.println("white  \t blanco");
  }
}
